package JavaPractise;

public class WhileLoop {

	public static void main(String[] args) {

		int i=1;
		while(i<=10){
			System.out.println(i);
			i++;
		}
		
		
		String arr1[]={"Venkat", "Naveen","Yarramneedi"};
		int j=0;
		while(j<arr1.length){
			System.out.println(arr1[j]);
			j++;
		}
		
	}

}
